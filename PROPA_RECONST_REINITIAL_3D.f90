                END IF
            END DO
            
            
            THETA_A = THETA_A - 2.*PI
        
            PHI_R = 14. * PI/180.
            PHI_C = 45. * PI/180.
            
            CHI_R = 2.*(TAN(PHI_R/2.))**2
            CHI_C = 2.*(TAN(PHI_C/2.))**2
            
