	ALLOCATE(FLUID_NORMAL(3,SURFACE_FLUID%SURFACE_FACES_NUM))
        FLUID_POINT_PRESSURE(:) = 0.
	FLUID_FACE_AREA(:) = 0.
	FLUID_NORMAL(:,:) = 0.
        
