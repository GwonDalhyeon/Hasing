        REAL(8), ALLOCATABLE :: N(:,:), A(:), W(:)
        REAL(8) :: W_SUM
        REAL(8) :: B(3)
        
        REAL(8) :: E_VALUE(3)
        REAL(8) :: E_VECTOR(3,3), DUMMY(3,3)
        
        INTEGER :: JMIN, TEMP, TEMP_INDEX
