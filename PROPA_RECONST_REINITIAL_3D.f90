    SUBROUTINE RESET_SURFACE(TYP, FLAG, POINT_NUM, FACE_NUM, SURFACE_POINTS, SURFACE_FACES, &
    SURFACE_INITIAL_FACE_AREA, SURFACE_INITIAL_EDGE_LENGTH, FACE_B_RATE, POINT_VELOCITY, POINT_DISPLACEMENT, &
