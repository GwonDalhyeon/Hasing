    INTEGER :: RIDGE_EDGE_NUM, SGN1, SGN2
    INTEGER, ALLOCATABLE :: RIDGE_EDGE(:,:)
        
        TYPE(SURFACE_TYPE), POINTER :: SURFACE_CURRENT
        
        IF (TYP==0) THEN
            SURFACE_CURRENT => SURFACE_FLUID
        END IF
