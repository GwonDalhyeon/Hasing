        INTEGER :: I1,I2,I3, CON_NUM
        REAL(8) :: V1(3),V2(3)
        REAL(8), ALLOCATABLE :: NORMAL(:,:)
        
        !! MODIFIED
        INTEGER :: LOCAL_SHAPE, TEMP_I, JMIN
